char	*ft_strcat(char *dest, char *src)
{
	char	*tmp;

	tmp = dest;
	while (dest[0])
		dest++;
	while (src[0])
	{
		dest[0] = src[0];
		src++;
		dest++;
	}
	dest[0] = 0;
	return (tmp);
}
