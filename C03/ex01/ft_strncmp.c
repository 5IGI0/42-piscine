int	ft_strncmp(char *s1, char *s2, unsigned int n)
{
	if (n == 0)
		return (0);
	n--;
	while (n && s1[0] && s2[0] && (s1[0] == s2[0]))
	{
		s1++;
		s2++;
		n--;
	}
	return ((int)((int)s1[0]) - ((int)s2[0]));
}
