int	ft_str_is_alpha(char *str)
{
	while (
		(str[0] <= 'z' && str[0] >= 'a')
		|| (str[0] <= 'Z' && str[0] >= 'A')
	)
	{
		str++;
	}
	return (str[0] == 0);
}
