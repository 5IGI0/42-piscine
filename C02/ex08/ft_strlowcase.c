char	*ft_strlowcase(char *str)
{
	char	*tmp;

	tmp = str;
	while (str[0] != 0)
	{
		if (str[0] >= 'A' && str[0] <= 'Z')
		{
			str[0] += 'a' - 'A';
		}
		str++;
	}
	return (tmp);
}
