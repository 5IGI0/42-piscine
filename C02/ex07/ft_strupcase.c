char	*ft_strupcase(char *str)
{
	char	*tmp;

	tmp = str;
	while (str[0] != 0)
	{
		if (str[0] >= 'a' && str[0] <= 'z')
		{
			str[0] -= 'a' - 'A';
		}
		str++;
	}
	return (tmp);
}
